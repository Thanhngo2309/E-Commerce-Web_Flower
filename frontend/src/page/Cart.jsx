import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/card'
import React, { use, useEffect } from 'react'
import userLogo from '../assets/userLogo.png'
import { useDispatch, useSelector } from 'react-redux'
import { Button } from '@/components/ui/button'
import { ShoppingCart, Trash2 } from 'lucide-react'
import { Separator } from '@/components/ui/separator'
import { Input } from '@/components/ui/input'
import { Link } from 'react-router-dom'
import axios from 'axios'
import { setCart } from '@/redux/productSlice'
import { toast } from 'sonner'
const Cart = () => {
    const { cart } = useSelector(store => store.product)
    const subtotal = cart?.totalPrice || 0
    const shipping = subtotal > 500000 ? 0 : 30000
    const tax = subtotal * 0.05
    const total = subtotal + shipping + tax
    const dispatch = useDispatch()
    const accessToken = localStorage.getItem('accessToken')

    const API = 'http://localhost:8000/api/v1/cart'

    const loadCart = async () => {
        try {
            const res = await axios.get(`${API}/get`, {
                headers: {
                    Authorization: `Bearer ${accessToken}`
                }
            })
            if (res.data.success) {
                dispatch(setCart(res.data.cart))
            }
        } catch (error) {
            console.log(error)
        }
    }
    const handleUpdateQuantity = async (productId, type) => {
        try {
            const res = await axios.put(`${API}/update`, { productId, type }, {
                headers: {
                    Authorization: `Bearer ${accessToken}`
                }
            })
            if (res.data.success) {
                dispatch(setCart(res.data.cart))
            }
        } catch (error) {
            console.log(error)
        }
    }
    
    const handleRemove = async (productId) => {
        try {
            const res = await axios.delete(`${API}/remove`, {
                data: { productId },
                headers: {
                    Authorization: `Bearer ${accessToken}`
                }
            })
            if (res.data.success) {
                dispatch(setCart(res.data.cart))
                toast.success("Product removed from cart")
            }
        } catch (error) {
            console.log(error)
        }
    }
    useEffect(() => {
        loadCart()
    },[dispatch])
    return (
        <div className='pt-30 bg-gray-50 min-h-screen'>
            {
                cart?.items?.length > 0 ? <div className='max-w-7xl mx-auto'>
                    <h1 className='text-2xl font-bold text-gray-800 mb-7'>Shopping Cart</h1>
                    <div className='max-w-7xl mx-auto flex gap-7'>
                        <div className='flex flex-col gap-5 flex-1'>
                            {cart?.items?.map((product, index) => {
                                return <Card key={index}>
                                    <div className='flex justify-between p-4 items-center'>
                                        <div className='flex gap-4 items-center w-[350px]'>
                                            <img src={product?.productId?.productImg?.[0]?.url || userLogo } alt={product.name} className='w-25 h-25' />
                                            <div className='w-[280px]'>
                                                <h1 className='font-semibold truncate'>{product?.productId?.productName}</h1>
                                                <p>{product?.productId?.productPrice} ₫</p>
                                            </div>
                                        </div>
                                        <div className='flex gap-5 items-center'>
                                            <Button onClick={() => handleUpdateQuantity(product.productId._id, 'decrease')} variant='outline'>-</Button>
                                            <span>{product.quantity}</span>
                                            <Button onClick={() => handleUpdateQuantity(product.productId._id, 'increase')} variant='outline'>+</Button>
                                        </div>
                                        <p>{(product?.productId?.productPrice) * (product?.quantity)} ₫</p>
                                        <p onClick={()=> handleRemove(product?.productId?._id)} className='text-red-500 items-center gap-1 cursor-pointer'> <Trash2 className='w-4 h-4'/>  Remove</p>
                                        
                                    </div>
                                </Card> 
                            })}
                        </div>
                        <div>
                            <Card className='w-[400px]'>
                                <CardHeader>
                                    <CardTitle>Order Summary</CardTitle>
                                </CardHeader>
                                <CardContent className='space-y-4'>
                                    <div className='flex justify-between'>
                                        <span>Subtotal ({cart?.items?.length} items)</span>
                                        <span>{cart?.totalPrice?.toLocaleString('en-US')} ₫</span>
                                    </div>
                                    <div className='flex justify-between'>
                                        <span>Shipping</span>
                                        <span>{shipping} ₫</span>
                                    </div>
                                    <div className='flex justify-between'>
                                        <span>Tax</span>
                                        <span>{tax} ₫</span>
                                    </div>
                                    
                                    <Separator/>
                                    <div className='flex justify-between font-bold text-lg'>
                                        <span>Total</span>
                                        <span>{total.toLocaleString('en-US')} ₫</span>
                                    </div>      
                                    <div className='space-y-3 pt-4'>
                                        <div className='flex space-x-2'>
                                            <Input placeholder='Promo code' />
                                            <Button variant='outline'>Apply</Button>
                                        </div>
                                        <Button className='bg-pink-600 text-white w-full'>PLACE ORDER</Button>
                                        <Button variant='outline' className='w-full'>
                                            <Link to='/products'>CONTINUE SHOPPING</Link>
                                        </Button>                 
                                    </div>                          
                                <div className='text-sm text-muted-foreground pt-4'>
                                    <p>* Free shipping on orders over 500,000 ₫</p>
                                    <p>* 30-day return policy</p>
                                    <p>* 24/7 customer support</p>
                                </div>
                                    
                                </CardContent>
                            </Card>
                        </div>
                    </div>
                </div> : <div className='flex flex-col items-center justify-center min-h-[60vh] p-6 text-center'>
                    {/* Icon */}
                    <div className='bg-pink-100 p-6 rounded-full'>
                        <ShoppingCart className='w-16 h-16 text-pink-600' />
                    </div>
                    {/* Title */}
                    <h2 className='text-2xl font-bold mt-4 text-pink-600'>Your Cart is Empty</h2>
                    <p className='text-muted-foreground mt-2 text-gray-600'>Looks like you haven't added anything to your cart yet.</p>
                    <Button className='mt-6 cursor-pointer bg-pink-600 text-white py-2 px-4 hover:bg-pink-700'><Link to='/products'>Start Shopping</Link></Button>
                </div>
            }
        </div>
    )
}

export default Cart